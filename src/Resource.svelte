<h2>Level 1 - Russian Numbers</h2>

<p> One - "A-deen" - один </p>
<p> Two - "Dva" - два </p>
<p> Three - "Tri" - три </p>
<p> Four - "Chi-ty-re" - Четыре </p>
<p> Five - "Pya-st" - пять </p>
<p> Six - "Shae-st" - шесть </p>
<p> Seven - "Sem" - Семь </p>
<p> Eight - "Vo-Sem" - восемь</p>
<p> Nine - "Dev-yat" - девять </p>
<p> Ten - "Des-yat" - десять</p>

